DROP TABLE IF EXISTS ODS_BLPT.T_HAND_REPAIR;
create table ODS_BLPT.T_HAND_REPAIR
(
   ID BIGINT,
  SEQ_ID STRING COMMENT '序号ID',
  RGN_NAME STRING COMMENT '大区名称',
  BASE_NAME STRING COMMENT '基地名称',
  BRAND_NAME STRING COMMENT '品牌名称',
  PRO_NAME STRING COMMENT '项目名称',
  REPAIR_ID STRING COMMENT '返修批次',
  REPAIR_ISSUE STRING COMMENT '返修问题',
  REPAIR_START_DAY STRING COMMENT '返修开始时间',
  REPAIR_CNT STRING COMMENT '返修总数',
  REPAIR_DONR_CNT STRING COMMENT '返修完成数',
  LOCATION STRING COMMENT '返修点',
  MV_CNT STRING COMMENT '移库工厂数量（若返修点为工厂，填写该栏）',
  IN_STORE_CNT STRING COMMENT '已回库房数量（若返修点为工厂，填写该栏）',
  LAST_UPDATE_DATE STRING,
  LAST_UPDATED_BY STRING,
  CREATION_DATE STRING,
  CREATED_BY STRING
) ROW FORMAT DELIMITED FIELDS TERMINATED BY '\036' STORED AS PARQUET;


